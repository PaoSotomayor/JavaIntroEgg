package EjerciciosAprendizaje;

import java.util.Scanner;

public class ejercicio04 {

/*Dada una cantidad de grados centígrados se debe mostrar su equivalente en 
rados Fahrenheit. La fórmula correspondiente es: F = 32 + (9 * C / 5).
public class ejercicio04 {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        Scanner leer = new Scanner(System.in);
        double cent, far;
        System.out.println("Ingrese la temperatura en grados centígrados para mostrar su equivalencia en Fahrenheit ");
        cent = leer.nextDouble();
        far = 32 + (9 * cent / 5);
        
        System.out.println("El equivalente en Fahrenheit es: " + far);
        
                
        
    }
    
}
